CREATE OR REPLACE PROCEDURE SP_INSERTAR_SUBCATEGORIA (
    P_ID_SUBCATEGORIA IN SUBCATEGORIA.ID_SUBCATEGORIA%TYPE,
    P_ID_CATEGORIA    IN SUBCATEGORIA.ID_CATEGORIA%TYPE,
    P_NOMBRE          IN SUBCATEGORIA.NOMBRE%TYPE,
    P_DESCRIPCION     IN SUBCATEGORIA.DESCRIPCION%TYPE
) AS
    V_EXISTE NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_EXISTE
    FROM CATEGORIA WHERE ID_CATEGORIA = P_ID_CATEGORIA;

    IF V_EXISTE = 0 THEN
        RAISE_APPLICATION_ERROR(-20011, 'No existe la categoría: ' || P_ID_CATEGORIA);
    END IF;

    INSERT INTO SUBCATEGORIA (
        ID_SUBCATEGORIA, ID_CATEGORIA,
        NOMBRE, DESCRIPCION, ACTIVA, SUB_POR_DEFECTO
    ) VALUES (
        P_ID_SUBCATEGORIA, P_ID_CATEGORIA,
        P_NOMBRE, P_DESCRIPCION, 1, 0
    );
    COMMIT;
EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        RAISE_APPLICATION_ERROR(-20001, 'Ya existe una subcategoría con ese ID.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_INSERTAR_SUBCATEGORIA;


-----------------------
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_SUBCATEGORIA (
    P_ID_SUBCATEGORIA IN SUBCATEGORIA.ID_SUBCATEGORIA%TYPE
) AS
    V_ID_CATEGORIA SUBCATEGORIA.ID_CATEGORIA%TYPE;
    V_ACTIVAS      NUMBER;
BEGIN
    SELECT ID_CATEGORIA INTO V_ID_CATEGORIA
    FROM SUBCATEGORIA WHERE ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;

    SELECT COUNT(*) INTO V_ACTIVAS
    FROM SUBCATEGORIA
    WHERE ID_CATEGORIA = V_ID_CATEGORIA AND ACTIVA = 1;

    IF V_ACTIVAS <= 1 THEN
        RAISE_APPLICATION_ERROR(-20012,
            'No se puede desactivar: es la única subcategoría activa de la categoría.');
    END IF;

    UPDATE SUBCATEGORIA SET ACTIVA = 0
    WHERE ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró la subcategoría: ' || P_ID_SUBCATEGORIA);
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_ELIMINAR_SUBCATEGORIA;




-- PROCEDURES: PRESUPUESTO Y PRESUPUESTO_DETALLE


CREATE OR REPLACE PROCEDURE SP_INSERTAR_PRESUPUESTO (
    P_ID_PRESUPUESTO     IN PRESUPUESTO.ID_PRESUPUESTO%TYPE,
    P_ID_USER            IN PRESUPUESTO.ID_USER%TYPE,
    P_NOMBRE_DESCRIPTIVO IN PRESUPUESTO.NOMBRE_DESCRIPTIVO%TYPE,
    P_ANIO_INICIO        IN PRESUPUESTO.ANIO_INICIO%TYPE,
    P_MES_INICIO         IN PRESUPUESTO.MES_INICIO%TYPE,
    P_ANIO_FIN           IN PRESUPUESTO.ANIO_FIN%TYPE,
    P_MES_FIN            IN PRESUPUESTO.MES_FIN%TYPE
) AS
BEGIN
    IF P_ANIO_FIN < P_ANIO_INICIO OR
      (P_ANIO_FIN = P_ANIO_INICIO AND P_MES_FIN < P_MES_INICIO) THEN
        RAISE_APPLICATION_ERROR(-20020,
            'El período de fin no puede ser anterior al período de inicio.');
    END IF;

    INSERT INTO PRESUPUESTO (
        ID_PRESUPUESTO, ID_USER, NOMBRE_DESCRIPTIVO,
        ANIO_INICIO, MES_INICIO, ANIO_FIN, MES_FIN,
        TOTAL_INGRESOS, TOTAL_GASTOS, TOTAL_AHORRO,
        FECHA_HORA_CREACION, ESTADO_PRESUPUESTO
    ) VALUES (
        P_ID_PRESUPUESTO, P_ID_USER, P_NOMBRE_DESCRIPTIVO,
        P_ANIO_INICIO, P_MES_INICIO, P_ANIO_FIN, P_MES_FIN,
        0, 0, 0, SYSTIMESTAMP, 'activo'
    );
    COMMIT;
EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        RAISE_APPLICATION_ERROR(-20001, 'Ya existe un presupuesto con ese ID.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_INSERTAR_PRESUPUESTO;
/


