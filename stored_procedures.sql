-- No permite desactivar si es la única activa de su categoría
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_SUBCATEGORIA (
    P_ID_SUBCATEGORIA IN SUBCATEGORIA.ID_SUBCATEGORIA%TYPE
) AS
    V_ID_CATEGORIA SUBCATEGORIA.ID_CATEGORIA%TYPE;
    V_ACTIVAS      NUMBER;
    V_TIENE_TRX    NUMBER;
BEGIN
    SELECT ID_CATEGORIA INTO V_ID_CATEGORIA
    FROM   SUBCATEGORIA WHERE ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;

    SELECT COUNT(*) INTO V_TIENE_TRX
    FROM   TRANSACCION WHERE ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;
    IF V_TIENE_TRX > 0 THEN
        RAISE_APPLICATION_ERROR(-20122,
            'No se puede desactivar: tiene ' || V_TIENE_TRX || ' transacción(es) asociada(s).');
    END IF;

    SELECT COUNT(*) INTO V_ACTIVAS
    FROM   SUBCATEGORIA WHERE ID_CATEGORIA = V_ID_CATEGORIA AND ACTIVA = 1;
    IF V_ACTIVAS <= 1 THEN
        RAISE_APPLICATION_ERROR(-20123,
            'No se puede desactivar: es la única subcategoría activa de la categoría.');
    END IF;

    UPDATE SUBCATEGORIA SET ACTIVA = 0 WHERE ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró la subcategoría: ' || P_ID_SUBCATEGORIA);
    WHEN OTHERS THEN ROLLBACK; RAISE;
END SP_ELIMINAR_SUBCATEGORIA;
/

CREATE OR REPLACE PROCEDURE SP_CONSULTAR_SUBCATEGORIA (
    P_ID_SUBCATEGORIA IN  SUBCATEGORIA.ID_SUBCATEGORIA%TYPE,
    P_CURSOR          OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN P_CURSOR FOR
        SELECT S.*, C.NOMBRE AS NOMBRE_CATEGORIA, C.TIPO
        FROM   SUBCATEGORIA S
        JOIN   CATEGORIA    C ON S.ID_CATEGORIA = C.ID_CATEGORIA
        WHERE  S.ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;
END SP_CONSULTAR_SUBCATEGORIA;
/

CREATE OR REPLACE PROCEDURE SP_LISTAR_SUBCATEGORIAS (
    P_ID_CATEGORIA IN  SUBCATEGORIA.ID_CATEGORIA%TYPE,
    P_CURSOR       OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN P_CURSOR FOR
        SELECT S.*, C.NOMBRE AS NOMBRE_CATEGORIA, C.TIPO
        FROM   SUBCATEGORIA S
        JOIN   CATEGORIA    C ON S.ID_CATEGORIA = C.ID_CATEGORIA
        WHERE  S.ID_CATEGORIA = P_ID_CATEGORIA
        AND    S.ACTIVA = 1
        ORDER  BY S.SUB_POR_DEFECTO DESC, S.NOMBRE;
END SP_LISTAR_SUBCATEGORIAS;
