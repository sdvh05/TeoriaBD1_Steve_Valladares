-- STORED PROCEDURES: TRANSACCION Y OBLIGACION_FIJA

CREATE OR REPLACE PROCEDURE SP_INSERTAR_TRANSACCION (
    P_ID_TRANSACCION  IN TRANSACCION.ID_TRANSACCION%TYPE,
    P_ID_USER         IN TRANSACCION.ID_USER%TYPE,
    P_ID_PRESUPUESTO  IN TRANSACCION.ID_PRESUPUESTO%TYPE,
    P_ID_SUBCATEGORIA IN TRANSACCION.ID_SUBCATEGORIA%TYPE,
    P_ANIO            IN TRANSACCION.ANIO%TYPE,
    P_MES             IN TRANSACCION.MES%TYPE,
    P_TIPO            IN TRANSACCION.TIPO%TYPE,
    P_DESCRIPCION     IN TRANSACCION.DESCRIPCION%TYPE,
    P_MONTO           IN TRANSACCION.MONTO%TYPE,
    P_FECHA           IN TRANSACCION.FECHA_TRANSACCION%TYPE,
    P_METODO_PAGO     IN TRANSACCION.METODO_PAGO%TYPE,
    P_NUM_FACTURA     IN TRANSACCION.NUM_FACTURA%TYPE,
    P_OBSERVACIONES   IN TRANSACCION.OBSERVACIONES%TYPE
) AS
    V_TIPO_SUBCAT VARCHAR2(10);
BEGIN
    -- Validar tipo vs subcategoría
    SELECT C.TIPO INTO V_TIPO_SUBCAT
    FROM   SUBCATEGORIA S
    JOIN   CATEGORIA    C ON S.ID_CATEGORIA = C.ID_CATEGORIA
    WHERE  S.ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;

    IF V_TIPO_SUBCAT != P_TIPO THEN
        RAISE_APPLICATION_ERROR(-20031,
            'El tipo de transacción no coincide con el tipo de la subcategoría.');
    END IF;

    INSERT INTO TRANSACCION (
        ID_TRANSACCION, ID_USER, ID_PRESUPUESTO,
        ID_SUBCATEGORIA, ANIO, MES, TIPO,
        DESCRIPCION, MONTO, FECHA_TRANSACCION,
        METODO_PAGO, NUM_FACTURA, OBSERVACIONES,
        FECHA_HORA_REGISTRO
    ) VALUES (
        P_ID_TRANSACCION, P_ID_USER, P_ID_PRESUPUESTO,
        P_ID_SUBCATEGORIA, P_ANIO, P_MES, P_TIPO,
        P_DESCRIPCION, P_MONTO, P_FECHA,
        P_METODO_PAGO, P_NUM_FACTURA, P_OBSERVACIONES,
        SYSTIMESTAMP
    );
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró el presupuesto o subcategoría.');
    WHEN DUP_VAL_ON_INDEX THEN
        RAISE_APPLICATION_ERROR(-20001, 'Ya existe una transacción con ese ID.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_INSERTAR_TRANSACCION;
/


-----------------------
CREATE OR REPLACE PROCEDURE SP_ELIMINAR_TRANSACCION (
    P_ID_TRANSACCION IN TRANSACCION.ID_TRANSACCION%TYPE
) AS
BEGIN
    DELETE FROM OBLIGACION_TRANSACCION WHERE ID_TRANSACCION = P_ID_TRANSACCION;
    DELETE FROM TRANSACCION            WHERE ID_TRANSACCION = P_ID_TRANSACCION;

    IF SQL%ROWCOUNT = 0 THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró la transacción: ' || P_ID_TRANSACCION);
    END IF;
    COMMIT;
EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_ELIMINAR_TRANSACCION;
/


-----------------------
CREATE OR REPLACE PROCEDURE SP_INSERTAR_OBLIGACION (
    P_ID_OBLIGACION   IN OBLIGACION_FIJA.ID_OBLIGACION%TYPE,
    P_ID_USER         IN OBLIGACION_FIJA.ID_USER%TYPE,
    P_ID_SUBCATEGORIA IN OBLIGACION_FIJA.ID_SUBCATEGORIA%TYPE,
    P_NOMBRE          IN OBLIGACION_FIJA.NOMBRE%TYPE,
    P_DESCRIPCION     IN OBLIGACION_FIJA.DESCRIPCION%TYPE,
    P_MONTO           IN OBLIGACION_FIJA.MONTO%TYPE,
    P_DIA             IN OBLIGACION_FIJA.DIA%TYPE,
    P_FECHA_INICIO    IN OBLIGACION_FIJA.FECHA_INICIO%TYPE,
    P_FECHA_FIN       IN OBLIGACION_FIJA.FECHA_FIN%TYPE
) AS
    V_TIPO VARCHAR2(10);
BEGIN
    SELECT C.TIPO INTO V_TIPO
    FROM   SUBCATEGORIA S
    JOIN   CATEGORIA    C ON S.ID_CATEGORIA = C.ID_CATEGORIA
    WHERE  S.ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;

    IF V_TIPO != 'gasto' THEN
        RAISE_APPLICATION_ERROR(-20040,
            'Las obligaciones fijas solo aplican a subcategorías de tipo gasto.');
    END IF;

    INSERT INTO OBLIGACION_FIJA (
        ID_OBLIGACION, ID_USER, ID_SUBCATEGORIA,
        NOMBRE, DESCRIPCION, MONTO,
        DIA, VIGENTE, FECHA_INICIO, FECHA_FIN
    ) VALUES (
        P_ID_OBLIGACION, P_ID_USER, P_ID_SUBCATEGORIA,
        P_NOMBRE, P_DESCRIPCION, P_MONTO,
        P_DIA, 1, P_FECHA_INICIO, P_FECHA_FIN
    );
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró la subcategoría: ' || P_ID_SUBCATEGORIA);
    WHEN DUP_VAL_ON_INDEX THEN
        RAISE_APPLICATION_ERROR(-20001, 'Ya existe una obligación con ese ID.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
END SP_INSERTAR_OBLIGACION;
/


-----------------------
