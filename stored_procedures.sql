CREATE OR REPLACE PROCEDURE SP_INSERTAR_OBLIGACION (
    P_ID_USER         IN  OBLIGACION_FIJA.ID_USER%TYPE,
    P_ID_SUBCATEGORIA IN  OBLIGACION_FIJA.ID_SUBCATEGORIA%TYPE,
    P_NOMBRE          IN  OBLIGACION_FIJA.NOMBRE%TYPE,
    P_DESCRIPCION     IN  OBLIGACION_FIJA.DESCRIPCION%TYPE,
    P_MONTO           IN  OBLIGACION_FIJA.MONTO%TYPE,
    P_DIA             IN  OBLIGACION_FIJA.DIA%TYPE,
    P_FECHA_INICIO    IN  OBLIGACION_FIJA.FECHA_INICIO%TYPE,
    P_FECHA_FIN       IN  OBLIGACION_FIJA.FECHA_FIN%TYPE,
    P_ID_GENERADO     OUT OBLIGACION_FIJA.ID_OBLIGACION%TYPE
) AS
    V_TIPO VARCHAR2(10);
BEGIN
    IF TRIM(P_NOMBRE) IS NULL THEN
        RAISE_APPLICATION_ERROR(-20160, 'El nombre de la obligación es obligatorio.');
    END IF;
    IF P_MONTO <= 0 THEN
        RAISE_APPLICATION_ERROR(-20161, 'El monto debe ser mayor a cero.');
    END IF;
    IF P_DIA NOT BETWEEN 1 AND 31 THEN
        RAISE_APPLICATION_ERROR(-20162, 'El día de vencimiento debe estar entre 1 y 31.');
    END IF;
    IF P_FECHA_FIN IS NOT NULL AND P_FECHA_FIN <= P_FECHA_INICIO THEN
        RAISE_APPLICATION_ERROR(-20163, 'La fecha fin debe ser mayor a la fecha inicio.');
    END IF;

    SELECT C.TIPO INTO V_TIPO
    FROM   SUBCATEGORIA S
    JOIN   CATEGORIA    C ON S.ID_CATEGORIA = C.ID_CATEGORIA
    WHERE  S.ID_SUBCATEGORIA = P_ID_SUBCATEGORIA;

    IF V_TIPO != 'gasto' THEN
        RAISE_APPLICATION_ERROR(-20164, 'Las obligaciones fijas solo aplican a subcategorías de tipo gasto.');
    END IF;

    INSERT INTO OBLIGACION_FIJA (
        ID_OBLIGACION, ID_USER, ID_SUBCATEGORIA,
        NOMBRE, DESCRIPCION, MONTO,
        DIA, VIGENTE, FECHA_INICIO, FECHA_FIN
    ) VALUES (
        NULL, P_ID_USER, P_ID_SUBCATEGORIA,
        P_NOMBRE, P_DESCRIPCION, P_MONTO,
        P_DIA, 1, P_FECHA_INICIO, P_FECHA_FIN
    ) RETURNING ID_OBLIGACION INTO P_ID_GENERADO;
    COMMIT;
EXCEPTION
    WHEN NO_DATA_FOUND THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se encontró la subcategoría: ' || P_ID_SUBCATEGORIA);
    WHEN OTHERS THEN ROLLBACK; RAISE;
END SP_INSERTAR_OBLIGACION;
/

CREATE OR REPLACE PROCEDURE SP_ACTUALIZAR_OBLIGACION (
    P_ID_OBLIGACION   IN OBLIGACION_FIJA.ID_OBLIGACION%TYPE,
    P_NOMBRE          IN OBLIGACION_FIJA.NOMBRE%TYPE,
    P_DESCRIPCION     IN OBLIGACION_FIJA.DESCRIPCION%TYPE,
    P_MONTO           IN OBLIGACION_FIJA.MONTO%TYPE,
    P_DIA             IN OBLIGACION_FIJA.DIA%TYPE
) AS
BEGIN
    IF P_MONTO <= 0 THEN
        RAISE_APPLICATION_ERROR(-20161, 'El monto debe ser mayor a cero.');
    END IF;
    IF P_DIA NOT BETWEEN 1 AND 31 THEN
        RAISE_APPLICATION_ERROR(-20162, 'El día debe estar entre 1 y 31.');
    END IF;

    UPDATE OBLIGACION_FIJA SET
