CREATE OR REPLACE PROCEDURE SP_RESUMEN_PRESUPUESTO_MES (
    P_ID_PRESUPUESTO IN PRESUPUESTO.ID_PRESUPUESTO%TYPE,
    P_ANIO IN NUMBER,
    P_MES IN  NUMBER,
    P_CURSOR OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN P_CURSOR FOR
        SELECT
            C.ID_CATEGORIA,
            C.NOMBRE AS NOMBRE_CATEGORIA,
            C.TIPO,
            C.COLOR_HEX,
            C.NOMBRE_ICON_UI,
            S.ID_SUBCATEGORIA,
            S.NOMBRE AS NOMBRE_SUBCATEGORIA,
            NVL(D.MONTO, 0) AS MONTO_PRESUPUESTADO,
            FN_CALCULAR_MONTO_EJECUTADO(S.ID_SUBCATEGORIA, P_ANIO, P_MES) AS MONTO_EJECUTADO,
            FN_OBTENER_BALANCE_SUBCATEGORIA(P_ID_PRESUPUESTO, S.ID_SUBCATEGORIA, P_ANIO, P_MES) AS BALANCE,
            FN_CALCULAR_PORCENTAJE_EJECUTADO(S.ID_SUBCATEGORIA, P_ID_PRESUPUESTO, P_ANIO, P_MES) AS PORCENTAJE
        FROM CATEGORIA C
        INNER JOIN   SUBCATEGORIA S ON S.ID_CATEGORIA = C.ID_CATEGORIA
        LEFT JOIN PRESUPUESTO_DETALLE D ON D.ID_SUBCATEGORIA = S.ID_SUBCATEGORIA
                                       AND D.ID_PRESUPUESTO  = P_ID_PRESUPUESTO
        WHERE  S.ACTIVA = 1
        ORDER  BY C.TIPO, C.ORDEN, C.NOMBRE, S.NOMBRE;
END SP_RESUMEN_PRESUPUESTO_MES;
/




