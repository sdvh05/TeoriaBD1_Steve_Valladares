CREATE OR REPLACE PROCEDURE SP_CREAR_PRESUPUESTO_COMPLETO (
    P_ID_USER IN PRESUPUESTO.ID_USER%TYPE,
    P_NOMBRE_DESCRIPTIVO IN  PRESUPUESTO.NOMBRE_DESCRIPTIVO%TYPE,
    P_ANIO_INICIO IN PRESUPUESTO.ANIO_INICIO%TYPE,
    P_MES_INICIO IN PRESUPUESTO.MES_INICIO%TYPE,
    P_ANIO_FIN IN PRESUPUESTO.ANIO_FIN%TYPE,
    P_MES_FIN IN PRESUPUESTO.MES_FIN%TYPE,
    P_DETALLES_JSON IN CLOB,
    P_ID_GENERADO OUT PRESUPUESTO.ID_PRESUPUESTO%TYPE
) AS
    -- Variables JSON
    V_ARRAY JSON_ARRAY_T;
    V_ITEM JSON_OBJECT_T;
    V_ID_SUBCAT VARCHAR2(10);
    V_MONTO NUMBER;
    V_JUSTIF VARCHAR2(100);
    V_ID_DETALLE VARCHAR2(10);
    V_COUNT NUMBER;
    V_ESTADO VARCHAR2(10);

BEGIN
    -- validar presupuesto
    IF TRIM(P_NOMBRE_DESCRIPTIVO) IS NULL THEN
        RAISE_APPLICATION_ERROR(-20130, 'El nombre descriptivo es obligatorio.');
    END IF;
    IF P_ANIO_INICIO < 2000 OR P_ANIO_FIN < 2000 THEN
        RAISE_APPLICATION_ERROR(-20131, 'El año debe ser mayor a 2000.');
    END IF;
    IF P_ANIO_FIN < P_ANIO_INICIO OR
      (P_ANIO_FIN = P_ANIO_INICIO AND P_MES_FIN < P_MES_INICIO) THEN
        RAISE_APPLICATION_ERROR(-20132, 'El período fin no puede ser anterior al inicio.');
    END IF;
    IF P_DETALLES_JSON IS NULL OR LENGTH(TRIM(P_DETALLES_JSON)) = 0 THEN
        RAISE_APPLICATION_ERROR(-20135, 'Debe incluir al menos un detalle en el JSON.');
    END IF;

    --parsear y validar json
    BEGIN
        V_ARRAY := JSON_ARRAY_T.PARSE(P_DETALLES_JSON);
    EXCEPTION
        WHEN OTHERS THEN
            RAISE_APPLICATION_ERROR(-20136, 'El JSON de detalles tiene formato inválido.');
    END;

    IF V_ARRAY.GET_SIZE() = 0 THEN
        RAISE_APPLICATION_ERROR(-20135, 'El JSON de detalles está vacío.');
    END IF;

    FOR I IN 0 .. V_ARRAY.GET_SIZE() - 1 LOOP
        V_ITEM := TREAT(V_ARRAY.GET(I) AS JSON_OBJECT_T);
        V_ID_SUBCAT := V_ITEM.GET_STRING('id_subcategoria');
        V_MONTO := V_ITEM.GET_Number('monto');

        IF V_ID_SUBCAT IS NULL THEN
            RAISE_APPLICATION_ERROR(-20137,
                'El elemento ' || (I+1) || ' del JSON no tiene "id_subcategoria".');
        END IF;
        IF V_MONTO IS NULL OR V_MONTO <= 0 THEN
            RAISE_APPLICATION_ERROR(-20140,
                'El elemento ' || (I+1) || ' del JSON tiene monto inválido (debe ser > 0).');
        END IF;

        --existe subcat
        SELECT COUNT(*) INTO V_COUNT
        FROM   SUBCATEGORIA
        WHERE  ID_SUBCATEGORIA = V_ID_SUBCAT AND ACTIVA = 1;

        IF V_COUNT = 0 THEN
            RAISE_APPLICATION_ERROR(-20002,
                'La subcategoría "' || V_ID_SUBCAT || '" no existe o está inactiva.');
        END IF;
    END LOOP;

    -- insert
    INSERT INTO PRESUPUESTO (
        ID_PRESUPUESTO, ID_USER, NOMBRE_DESCRIPTIVO,
        ANIO_INICIO, MES_INICIO, ANIO_FIN, MES_FIN,
        TOTAL_INGRESOS, TOTAL_GASTOS, TOTAL_AHORRO,
        FECHA_HORA_CREACION, ESTADO_PRESUPUESTO
    ) VALUES (
        NULL, P_ID_USER, P_NOMBRE_DESCRIPTIVO,
        P_ANIO_INICIO, P_MES_INICIO, P_ANIO_FIN, P_MES_FIN,
        0, 0, 0, SYSTIMESTAMP, 'activo'
    ) RETURNING ID_PRESUPUESTO INTO P_ID_GENERADO;

    --insert json
    FOR I IN 0 .. V_ARRAY.GET_SIZE() - 1 LOOP
        V_ITEM := TREAT(V_ARRAY.GET(I) AS JSON_OBJECT_T);
        V_ID_SUBCAT := V_ITEM.GET_STRING('id_subcategoria');
        V_MONTO := V_ITEM.GET_Number('monto');

        IF V_ITEM.HAS('justificacion') THEN
            V_JUSTIF := V_ITEM.GET_STRING('justificacion');
        ELSE
            V_JUSTIF := NULL;
        END IF;

        INSERT INTO PRESUPUESTO_DETALLE (
            ID_DETALLE, ID_PRESUPUESTO, ID_SUBCATEGORIA, MONTO, JUSTIFICACION
        ) VALUES (
            NULL, P_ID_GENERADO, V_ID_SUBCAT, V_MONTO, V_JUSTIF
        ) RETURNING ID_DETALLE INTO V_ID_DETALLE;
    END LOOP;

 
    COMMIT;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK; 
        RAISE;
END SP_CREAR_PRESUPUESTO_COMPLETO;
/
